/**
 * GEO Content Transformation - rewritten drafts optimized for
 * clarity, trust, medical authority, and AI citation readiness.
 * Each transform includes: diagnosis → structure → draft → changelog.
 */

export interface ContentDiagnosis {
  strengths: string[];
  weaknesses: string[];
  geoBlockers: string[];        // specific reasons AI won't cite this page today
  parentExperience: string;     // how a worried parent perceives this page
}

export interface RecommendedStructure {
  sections: { tag: string; heading: string; purpose: string }[];
  schemaTypes: string[];
  wordCountTarget: string;
  tone: string;
}

export interface DraftSection {
  heading: string;
  tag: 'h1' | 'h2' | 'h3';
  content: string;   // Hebrew markdown-like prose
}

export interface ChangeLogItem {
  area: string;
  before: string;
  after: string;
  reason: string;
}

export interface ContentTransform {
  pageId: string;               // matches RewriteBrief.id
  diagnosis: ContentDiagnosis;
  recommendedStructure: RecommendedStructure;
  draft: DraftSection[];
  changeLog: ChangeLogItem[];
}

